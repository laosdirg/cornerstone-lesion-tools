let cornerstone = window.cornerstone;
let cornerstoneTools = window.cornerstoneTools;

export default {
  set cornerstone (cst) {
    cornerstone = cst;
  },
  get cornerstone () {
    return cornerstone;
  },
  set cornerstoneTools (cst) {
    cornerstoneTools = cst;
  },
  get cornerstoneTools () {
    return cornerstoneTools;
  }
};
